package bean;

public class Lender {
  private Long id;
  private String name;
  private Long workid;

  public Long getId() {
    return id;
  }

  public void setId(Long id) {
    this.id = id;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public Long getWorkid() {
    return workid;
  }

  public void setWorkid(Long workid) {
    this.workid = workid;
  }
}
